#include "calc.h"

int Calculator::add(int a, int b)
{
    return a + b;
}

int Calculator::divide(int a, int b)
{
    return a / b;
}

int Calculator::multiply(int a, int b)
{
    return a * b;
}

int Calculator::square(int a)
{
    return a * a;
}

int Calculator::subtract(int a, int b)
{
    return a - b;
}